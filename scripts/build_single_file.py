    for sym, desc in checks.items():
        found = sym in result
        status = "✅" if found else "❌"
        print(f"  {status} {desc}: {sym}")
        if not found:
            all_ok = False
    print(f"\n  {'✅ All checks passed' if all_ok else '❌ Some checks failed'}")
    print("  Done ✅")